    <p><strong class="news_title">About Cystic Fibrosis:</strong></p>
    <p>
      Cystic fibrosis (CF) is a life-threatening disease that affects the lungs
      and digestive system and impacts about 70,000 people worldwide. CF is
      caused by mutations in the cystic fibrosis transmembrane conductance
      regulator (CFTR) gene which results in either no CFTR protein or an
      abnormal CFTR protein that does not function properly. This dysfunctional
      protein causes the body to accumulate excessive levels of unusually thick
      mucus in the lungs. This excessive sticky mucus becomes a site for
      recurrent infections that can require hospitalization. Respiratory
      distress in CF — defined as acute difficulty in breathing, infection
      and/or hospitalization — is most commonly related to mucus accumulation
      and lung infections that result in damage to lung tissue.
